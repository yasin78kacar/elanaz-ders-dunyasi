export function gorselSanatlarTema(temaNo, karistir) {
  const meta = GS_TEMA_META[temaNo - 1];
  const pool = POOLS[temaNo - 1];
  const onekA = `gs${temaNo}-a`;
  const onekT = `gs${temaNo}-t`;
  const genisA = [...pool.a];
  const genisT = [...pool.t];
  while (genisA.length < 50) genisA.push(...pool.a.map(([s, c, g, ip = '']) => [`${s} (${genisA.length + 1})`, c, g, ip]));
  while (genisT.length < 50) genisT.push(...pool.t.map(([s, c, sec, g, ip = '']) => [`${s} (${genisT.length + 1})`, c, sec, g, ip]));
  return {
    id: meta.id,
    baslik: meta.baslik,
    kazanimKodu: meta.kazanim,
    anlatim: {
      ekranlar: [
        {
          metin: `Elanaz Görsel Sanatlar dersinde ${meta.baslik.toLowerCase()} konusunu öğrendi. Renkler, formlar ve sanat teknikleri bir araya gelince güzel eserler ortaya çıkar.`,
          gorsel: anl(meta.anlatim),
        },
        {
          metin: 'Öğretmeni malzemeleri tanıttı ve sınıfta birlikte uygulama yaptılar. Elanaz dikkatle dinledi ve kendi çalışmasını oluşturdu.',
          gorsel: anl('malzemeler'),
        },
        {
          metin: 'Ders sonunda eserlerini sergilediler. Elanaz sanatın hem eğlenceli hem öğretici olduğunu anladı.',
          gorsel: anl('turk-sanatlari'),
        },
      ],
    },
    alistirma: sablonA(genisA.slice(0, 50), meta.kazanim, onekA),
    test: sablonT(genisT.slice(0, 50), meta.kazanim, onekT, karistir),
  };
}

