function dy(cevap) {
  return cevap === 'Doğru' || cevap === 'Evet'
    ? ['Evet', 'evet', 'doğru']
    : ['Hayır', 'hayır', 'yanlış'];
}

function sablonA(sablonlar, kazanim, onek) {
  return sablonlar.map(([soru, cevap, gorsel, ipucu = ''], i) => ({
    id: `${onek}-e${i + 1}`,
    kazanimKodu: kazanim,
    tip: 'yazili',
    soru,
    dogruCevap: String(cevap),
    ipucu,
    cevapTipi: 'metin',
    ...(cevap === 'Doğru' || cevap === 'Yanlış' || cevap === 'Evet' || cevap === 'Hayır'
      ? { alternatifCevaplar: dy(cevap) }
      : {}),
    gorsel,
  }));
}

function sablonT(sablonlar, kazanim, onek, karistir) {
  return sablonlar.map(([soru, cevap, secenekler, gorsel, ipucu = ''], i) => ({
    id: `${onek}-e${i + 1}`,
    kazanimKodu: kazanim,
    tip: 'coktanSecmeli',
    soru,
    dogruCevap: cevap,
    secenekler: karistir(secenekler),
    ipucu,
    gorsel,
  }));
}

